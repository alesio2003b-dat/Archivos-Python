#Determinación del delta M
def deltam(A,B,a,b,m,M):
    x=[]
    dm=[]
    masa=0
    for i in range(m,M+1):
        x.append(i)
    med=round((m+M)/2)
    for i in x:
        delta=abs((A*i+B)-(a*i+b))
        dm.append(delta)
        if i==med:
             masa=delta
    return [dm,masa]
def escribirarchivo(T,dm):
        with open("cobredmt.txt","a") as algo:
            algo.write(f"{T} {dm} \n")
